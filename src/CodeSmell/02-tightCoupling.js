/* ========== ALto Acoplamiento ==========
  - Lo ideal es tener un bajo acoplamiento.

  - Se refiere a cuan relacionadas o dependientes son dos clases o
    modulos entre si.

  - En bajo acoplamiento, cambiar algo importante en una clase no deberia afectar
    a la otra.

  - En alto acoplamiento, dificultaría el cambio y el mantenimiento de su código;
    dado que las clses estan muy unidas, hacer un cambio podria requerir una
    renovacion completa del sistema.

  - Desventajas de un Alto Acoplamiento:

    . Un cambio en un módulo por lo general provoca un efecto dominó de
      los cambios en otros módulos.

    . El ensamblaje de modulos puede requerir mas esfuerzo y/o tiempo
      debido a la mayor dependecia entre modulos.

    . Un modulo en particular puede ser mas dicificil de reutilizar y/o probar
      porque se deben incluir modulos dependientes.

  ============ Alta Cohesion =======================
  - Lo ideal es tener buena cohesion.

  - La cohesion se refiere a lo que la clase(o modulo) puede hacer.

  - la baja cohesion significa que la clase realiza una gran variedad de
    acciones: es amplia, no se enfoca en lo que debe hacer.

  - ALta cohesion significa que la clase se enfoca en lo que deberia estar
    haciendo, es decir, solo metodos relacionados con la intension de la clase

  ***** Un buen diseño de software tiene un bajo acoplamiento y una buena cohesion ******
*/
